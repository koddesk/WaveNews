import UIKit

extension UIFont {
    
    //Regular
    static func interRegular14() -> UIFont? {
        return UIFont(name: "Inter-Regular", size: 14)
    }
    
    static func interRegular16() -> UIFont? {
        return UIFont(name: "Inter-Regular", size: 16)
    }
    
    //Bold
    static func interBold16() -> UIFont? {
        return UIFont(name: "Inter-Bold", size: 16)
    }
    
    //Semi-Bold
    static func interSemiBold12() -> UIFont? {
        return UIFont(name: "Inter-SemiBold", size: 12)
    }
    
    static func interSemiBold16() -> UIFont? {
        return UIFont(name: "Inter-SemiBold", size: 16)
    }
    
    static func interSemiBold20() -> UIFont? {
        return UIFont(name: "Inter-SemiBold", size: 20)
    }
    
    static func interSemiBold24() -> UIFont? {
        return UIFont(name: "Inter-SemiBold", size: 24)
    }
}
